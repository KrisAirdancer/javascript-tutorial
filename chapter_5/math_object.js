// MATH OBJECT

// This logs all of the properties and methods in the Math class to the console. Cool!
console.log(Math);

console.log(Math.PI);
console.log(Math.E);

const area = 7.7;
console.log(Math.round(area));
console.log(Math.floor(area));
console.log(Math.ceil(area));
console.log(Math.trunc(area));

// Random Number Generation

const random = Math.random();

console.log(random);
console.log(Math.round(random)); // Generates 1s and 0s randomly
console.log(Math.round(random * 100)); // Generates numbers between 0 and 100

